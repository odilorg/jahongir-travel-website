												<p>Breakfast. Guided hike via Ukhum to Hayat village (12km/ 4 hours). See the plains of Kyzyl-Kum steppe from the top of Karul mount ruins of ‘Deh’ fortress. Arrive in Hayat village and settle in the homestay. Lunch. Walk around the village and get to know the daily livelihood activities of the people. Free time in the homestay. Dinner. Overnight stay in the homestay.</p>
