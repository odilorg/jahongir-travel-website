												<h2><strong>Day 3 Nuratau Mountains - Samarkand</strong></h2>
												<p>Breakfast. We drive to the old oasis of Nur, modern city Nurata. Nurata is located between Bukhara and Samarkand and it serves as the connecting hub between these destinations. The city was taken over by Alexander the Great in 4th century B.C. and became the fortified town to his North West part of his empire. We will visit the ruins of this fortified wall. Also as the name “Nur Ata” means 
												“Father of the Ray” the town contains the holy spring – “Chashma” with the mosque and other structures.  After the visit we have lunch at local chayhana (tea house) and continue to Samarkand.</p>
