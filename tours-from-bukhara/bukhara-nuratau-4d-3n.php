												<p>Meet at your hotel in Bukhara. On the way visit the world famous Gijduvan ceramics Museum and Narzullaev’s ceramic workshop. We will stop to see the ruins of Rabati Malik (one of most ancient caravanserai in Central Asia) which was built from 1068 to 1080 A.D. to serve as caravanserai on the Great Silk Road. Arrive in Hayat and settle in the homestay. Dinner. Overnight stay in the homestay</p>
												
