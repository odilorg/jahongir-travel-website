										<div class="interary-item">
											<p><span class="icon-left">3</span></p>
											<div class="item_content">
												<h2><strong>Day 3 Tashkent- Urgench - Khiva</strong></h2>
												<p>Breakfast. Transfer to the local airport and take a flight to Urgench. Transfer to Khiva (30 km). Installation for one night in a hotel located in Ichan - Kala (the interior and ancient part of the city). Guided tour of the city. Khiva is one of the remarkable cities of Khorezm Khanat (khanat - a big Empire).</p>
												<p> Being the capital of the Khanat in XVII century, it was one of the largest markets of Central Asia and its prosperity lasted until the beginning of the XX century. Today, the interior city Ichan Kala is a true open museum where one can admire the authenticity of the monuments Kounia-Ark (the residence of Khans) XVI century, the mausoleum of Pahlavan Mahmoud XV century, the Friday mosque, the madrasah of Khoja Islam XIX century, etc. The city is surrounded by pink clay fortifications. Overnight in Khiva.</p>
											</div>
										</div>
										<div class="interary-item">
											<p><span class="icon-left">4</span></p>
											<div class="item_content">
												<h2><strong>Day 4 Khiva –Bukhara</strong></h2>
												<p>Breakfast. You take a long drive (480 km, 9-10 hours) through the Kyzyl Kum desert along the banks of the Amudarya River to Bukhara. The rest of the day is devoted to checking in the hotel and leisure. The construction of the new road make the course little bit demanding and it takes about 9- 10 hours. Arrival in Bukhara in the late afternoon and accommodation at the hotel. Overnight in Bukhara</p>
											</div>
										</div>
										<div class="interary-item">
											<p><span class="icon-left">5</span></p>
											<div class="item_content">
												<h2><strong>Day 5 Bukhara</strong></h2>
												<p>Breakfast. Bukhara is a real Oriental fairytale, city with small romantic streets, unsurpassed sights that preserved the beauty and charm of the ancient walls. Monuments are located close to each other and that’s why sightseeing tour will be organized almost without use of vehicle. In this day you will see: Samanids’ dynasty mausoleum (IX-X cc), Chashma-Ayub burial-vault (XIV-XIX cc), Minorai Kalon ensemble, Madrassah Miri-Arab, Madrassah Ulugbek and Abdulazizkhan, Labi Hauz complex (XVI-XVII), Kukeldash Madrassah (1568), Arc Fortress (VI-VIIcc), Chor-Minor Madrassah, Visit will continue to the trade domes of ancient Bukhara bazaar - even if you won’t buy any souvenir or adornment, you will get many pleasant impressions about Orient and its inhabitants. Overnight in Bukhara.</p>
											</div>
										</div>
										<div class="interary-item">
											<p><span class="icon-left">6</span></p>
											<div class="item_content">
												<h2><strong>Day 6 Bukhara – Nurata – Yurt Camp</strong></h2>
												<p>Breakfast at the hotel. Transfer to the real yurt camp near the lake Aydarkul. Visit Nurata, where you will see: “Chashma” holy spring - Namozgokh Mosque (10 c) which was reconstructed by Emir Abdullah in 1582 - Sheykh Khasan Nuri Madrassah (16-17 cc) - ruins of Alexander the Great citadel. Check in the yurts. Dinner under the open sky sparkling with millions of stars. After dinner you will see fascinating folklore show: “Akyn” – singer of the national tunes will be singing for you. Overnight in yurts.</p>
											</div>
										</div>
										<div class="interary-item">
											<p><span class="icon-left">7</span></p>
											<div class="item_content">
												<h2><strong>Day 7 Yurt Camp – Samarkand</strong></h2>
												<p>Breakfast at the tent and then transfer (by camels or on vehicle) to Aydarkul Lake. You will have a lunch (lake fish) in a tent near lakeside of Aydarkul. You can enjoy the swimming in water of the unique Aydarkul Lake, relax laying in the sun or shade set closely to the lakeside. Departure yurt camp and transfer to Samarkand! Upon arrival you will have free time for rest of the day. Walk along mysterious and ancient Samarkand! Overnight in Samarkand.</p>
											</div>
										</div>
										<div class="interary-item">
											<p><span class="icon-left">8</span></p>
											<div class="item_content">
												<h2><strong>Day 8 Samarkand</strong></h2>
												<p>Breakfast at the hotel. Start sightseeing tour in “Pearl of the Orient”- that name was given to Samarkand by the philosophers and poets of the past. You will see this famous city in all its beauty : visit Registan square (XVII c) including Ulugbek Madrassah (1417-1420), Sher-Dor Madrassah (1619-1636), Tillya-Kori (1647-1660) Madrassah, Gur-Emir mausoleum , Bibi-Khanum mosque (XVc.), Shokhi-Zinda (XII-XVc) architectural assemble, Ulugbek observatory(XVc.). Overnight in Samarkand.</p>
											</div>
										</div>
										<div class="interary-item">
											<p><span class="icon-left">9</span></p>
											<div class="item_content">
												<h2><strong>Day 9 Samarkand - Shahrisabz</strong></h2>
												<p>Breakfast. We head South to the Native town of Amir Temur. The road goes over the beautiful pass where we have several stops. In Shahrisabz we see the magnificent palace of Timur - Ak Saray, then visit the unoccupied grave of Timur and the mausoleums of his two sons and his father. </p>
