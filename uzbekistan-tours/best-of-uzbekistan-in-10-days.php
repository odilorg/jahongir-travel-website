												<h2><strong>Day 2 Tashkent</strong></h2>
												<p>Breakfast. Visiting the new part of Tashkent: Independence Square (‘Mustakillik’) and Amir Temur Square. We advise you to use Tashkent’s metro during sightseeing tour as it’s one of the most beautiful: each station has particular non-recurrent design and could be compared with many ancient monuments. Overnight in Tashkent.</p>
											</div>
