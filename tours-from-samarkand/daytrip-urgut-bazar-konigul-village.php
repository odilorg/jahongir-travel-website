										<h2>Tour program daytrip to Urgut </h2>
										<p>This day tour offers the unique experience to explore the local art and craft by visiting the Samarkand Silk Carpet factory, Silk Paper factory in small village Konigil and the town Urgut.</p><p> Urgut is a small town in Uzbekistan around 40 km from Samarkand at the footsteps of Zeravshan mountain range. It is well known among the locals as one of the biggest wholesale “bazaars” markets in Samarkand region. It is also of the great interest to visitors as there is the 8th generation pottery workshop where one can practice the pottery skills. Another sight is the “Chor Chinor” complex – the thousand year old plane trees alley, uniquely preserved grove that survived till our days.</p> 
										<p>Konigil – is a small village near Samarkand. It is a home for a family of the local craftsmen who produces the silk paper using only the traditional technology like water mills and most of the work done by hand.
										The history   of the silk paper is dated back to 105 A.D. and was invented by Chinese. Thanks to the Silk Road the process of making paper was brought to Samarkand in 7th century. </p>
										<p>In this trip you will participate in paper making process and have an excursion around the paper factory. The factory is located in the very peaceful and charming part of the village on the banks of the Siab river. 
										</p>
										
