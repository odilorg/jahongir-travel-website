												<h2><strong>Toprak Qala</strong></h2>
												<p>The driver (guide optional) will pick you up from your hotel in Khiva. The first qala to reach is Toprak Qala which is 100 km from Khiva. Topraq qala means "Clay Fort" which standing over 20 m tall and is dated in the Kushan period, around 2nd and 3rd centuries AD.  Some historians assume that it was a royal summer or winter residence but not capital. Toprak qala was used until the 6th century AD with short periods when it was abandoned. Afterward, it was occupied by squatters till the Mongol invasion.</p>
												
											</div>
										</div>
										<div class="interary-item">
											<p><span class="icon-left">2</span></p>
											<div class="item_content">
												<h2><strong>Qizil Qala</strong></h2>
												<p>From Toprak qala we drive to Ayaz Qala but stop at Qizil qala on the way. Initially, archeologists assumed that it was a military barracks with troops but after discovering the opulent interior decoration it was clear that it belonged to an important Khorezmian official. </p>
											</div>
										</div>
										<div class="interary-item">
											<p><span class="icon-left">3</span></p>
											<div class="item_content">
												<h2><strong>Ayaz Qala</strong></h2>
												<p>Finally, we drive to Ayaz Qala which is located in a desert region of Ellikqala region. However, it was an oasis in the first millennium BC which was irrigated from the Akcha Darya river.  Ayaz Qala consists of three forts. Ayaz Qala 1 -a defensive fortress located on the hill built in the 4th century BC. Southwest of Ayaz Qala1 is located Ayaz Qala 2  a feudal fort dated 6-8th century BC built on a conical hill. </p>
											</div>
										</div>
										<div class="interary-item">
											<p><span class="icon-left">4</span></p>
											<div class="item_content">
												<h2><strong>Janbas Qala</strong></h2>
												<p>We will head back to Khiva and on the way back visit the Janbas Qala and it is a "must-see" one. The word means side because the fort is built on the side of the hill and not on the high point which would be more strategic and it still puzzles the scientists. Built-in 4th century BC measuring 200x170 meters it has walls reaching almost 20m in height and relatively good shape.  </p>
